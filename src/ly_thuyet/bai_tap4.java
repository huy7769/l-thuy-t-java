package ly_thuyet;
 // các phép toán một ngôi
public class bai_tap4 {
     public static void main(String[] args) {
	    int a = 6;
	    boolean b = false;
	    System.out.println("-a => Giá trị: "+ (-a));
	    System.out.println("+a => Giá trị: "+ (+a));
	    System.out.println("!b => Giá trị: "+ (!b));
	    System.out.println("----------");
	    System.out.println("a = " + a);
	    System.out.println("++a = "+ ++a);
	    System.out.println("a++ = " + a++);
	    System.out.println("a = " + a);
	    System.out.println("--a = "+ --a);
	    System.out.println("a-- = "+ a--);
	    System.out.println("a = " + a);
  }
}
